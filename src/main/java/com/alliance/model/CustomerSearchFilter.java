package com.alliance.model;

public class CustomerSearchFilter extends SearchFilter {
	private String id;
	private String name;
	private String street;
	private String city;
	private String state;
	private String zip;
	private String country;
	private String email;
	private String contact;
	private String phone;
	private String phoneType;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getPhoneType() {
		return phoneType;
	}

	public void setPhoneType(String phoneType) {
		this.phoneType = phoneType;
	}

	@Override
	public String toString() {
		return "CustomerSearchFilter [id=" + id + ", name=" + name + ", street=" + street + ", city=" + city + ", state=" + state + ", zip=" + zip + ", country=" + country + ", email=" + email + ", contact=" + contact + ", phone=" + phone + ", phoneType=" + phoneType + "]";
	}

}
